// Функция для получения семантических данных секции
export function getSectionSemantics(sectionId: string): SectionSemantics | undefined {
  return sectionSemanticsData.find((section) => section.sectionId === sectionId);
}

// Функция для генерации всех хэштегов лендинга
export function getAllHashtags(): string[] {
  const allHashtags = sectionSemanticsData.reduce((acc, section) => {
    return acc.concat(section.hashtags);
  }, [] as string[]);

  // Убираем дубликаты
  return [...new Set(allHashtags)];
}

// Функция для генерации всех ключевых слов
export function getAllKeywords(): string[] {
  const allKeywords = sectionSemanticsData.reduce((acc, section) => {
    return acc.concat(section.keywords);
  }, [] as string[]);

  // Убираем дубликаты
  return [...new Set(allKeywords)];
}

// Schema.org для изображения секции
export function generateImageSchema(sectionId: string) {
  const semantics = getSectionSemantics(sectionId);
  if (!semantics) return null;

  return {
    '@context': 'https://schema.org',
    '@type': 'ImageObject',
    name: semantics.patternImage.title,
    description: semantics.patternImage.alt,
    url: `https://zerodolg.ru${semantics.patternImage.url}`,
    contentUrl: `https://zerodolg.ru${semantics.patternImage.url}`,
    keywords: semantics.keywords.join(', '),
    about: {
      '@type': semantics.schemaType,
      name: semantics.title,
      description: semantics.description,
    },
  };
}
