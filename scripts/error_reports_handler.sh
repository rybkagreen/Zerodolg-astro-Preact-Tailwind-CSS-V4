#!/bin/bash

# Скрипт для обработки отчетов об ошибках
# Запускается при получении файла с удаленного сервера

REPORT_FILE="$1"

echo "Получен отчет об ошибке: $REPORT_FILE"

# Пример обработки отчета
if [ -f "$REPORT_FILE" ]; then
    echo "Обработка отчета об ошибке..."
    
    # Логируем содержимое отчета
    echo "Содержимое отчета:"
    cat "$REPORT_FILE"
    echo ""
    
    # Вместо вызова Qwen Code, можно выполнить другие действия
    # например, анализ отчета с помощью локально установленных инструментов
    echo "Выполняем обработку отчета об ошибке..."
    
    # После обработки можно отправить подтверждение обратно на удаленный сервер
    # или выполнить другие действия
    echo "Отчет об ошибке обработан локально"
    
    # Запускаем скрипт деплоя
    echo "Запускаем скрипт деплоя..."
    /root/develop/zerodolg.ru/zerodolg-astro/deploy-ssr.sh
else
    echo "Файл отчета не найден: $REPORT_FILE"
fi