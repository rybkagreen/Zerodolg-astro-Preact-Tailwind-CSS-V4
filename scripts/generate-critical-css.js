#!/usr/bin/env node

/**
 * Critical CSS Generation Script
 * Генерирует критический CSS для above-the-fold контента
 * Использует Puppeteer для анализа страницы и выделения критических стилей
 */

import { readFileSync, writeFileSync } from 'fs';
import { join, dirname } from 'path';
import { fileURLToPath } from 'url';
import puppeteer from 'puppeteer';

const __filename = fileURLToPath(import.meta.url);
const __dirname = dirname(__filename);

// Конфигурация
const config = {
  url: 'http://localhost:4321/', // URL для анализа
  viewports: [
    { width: 1920, height: 1080, name: 'desktop' },
    { width: 768, height: 1024, name: 'tablet' },
    { width: 375, height: 667, name: 'mobile' },
  ],
  outputPath: join(__dirname, '../src/app/styles/critical.css'),
  timeout: 30000,
};

/**
 * Извлекает критические стили для определенного viewport
 */
async function extractCriticalCSS(page, viewport) {
  await page.setViewport(viewport);
  await page.goto(config.url, { waitUntil: 'networkidle0', timeout: config.timeout });

  // Получаем все CSS правила, которые применяются к элементам above-the-fold
  const criticalStyles = await page.evaluate(() => {
    const criticalSelectors = new Set();
    const viewportHeight = window.innerHeight;

    // Получаем все элементы в пределах viewport
    const elementsInViewport = Array.from(document.querySelectorAll('*')).filter((el) => {
      const rect = el.getBoundingClientRect();
      return rect.top < viewportHeight && rect.bottom > 0;
    });

    // Для каждого элемента получаем примененные стили
    elementsInViewport.forEach((element) => {
      const computedStyle = window.getComputedStyle(element);
      const rules = document.styleSheets[0]?.cssRules || [];

      // Проверяем какие правила применяются к элементу
      for (let rule of rules) {
        if (rule.selectorText && element.matches && element.matches(rule.selectorText)) {
          criticalSelectors.add(rule.cssText);
        }
      }
    });

    return Array.from(criticalSelectors);
  });

  return criticalStyles;
}

/**
 * Генерирует критический CSS для всех viewport'ов
 */
async function generateCriticalCSS() {
  console.log('🚀 Запуск генерации критического CSS...');

  const browser = await puppeteer.launch({
    headless: 'new',
    args: ['--no-sandbox', '--disable-setuid-sandbox'],
  });

  try {
    const page = await browser.newPage();
    const allCriticalStyles = new Set();

    // Анализируем каждый viewport
    for (const viewport of config.viewports) {
      console.log(`📱 Анализ viewport: ${viewport.name} (${viewport.width}x${viewport.height})`);

      const styles = await extractCriticalCSS(page, viewport);
      styles.forEach((style) => allCriticalStyles.add(style));

      console.log(`✅ Найдено ${styles.length} критических стилей для ${viewport.name}`);
    }

    // Базовые критические стили, которые всегда нужны
    const baseCriticalCSS = `
/* Critical Above-the-fold CSS - Auto Generated */
/* DO NOT EDIT MANUALLY - This file is generated by scripts/generate-critical-css.js */

/* Reset and Base Styles */
*,*::before,*::after{box-sizing:border-box}
html{line-height:1.15;-webkit-text-size-adjust:100%;scroll-behavior:smooth}
body{margin:0;font-family:ui-sans-serif,system-ui,-apple-system,sans-serif;-webkit-font-smoothing:antialiased;-moz-osx-font-smoothing:grayscale}

/* Layout Containers */
.container-custom{max-width:80rem;margin-left:auto;margin-right:auto;padding-left:1rem;padding-right:1rem}
@media(min-width:640px){.container-custom{padding-left:1.5rem;padding-right:1.5rem}}
@media(min-width:1024px){.container-custom{padding-left:2rem;padding-right:2rem}}

/* Header and Navigation */
header{position:sticky;top:0;z-index:50;background:white;box-shadow:0 1px 3px rgba(0,0,0,.1)}
nav{display:flex;align-items:center;justify-content:space-between;height:5rem}

/* Hero Section */
.hero-section{position:relative;overflow:hidden;padding-top:5rem;padding-bottom:5rem;background:linear-gradient(135deg,#1e1b4b 0%,#312e81 50%,#3730a3 100%)}
@media(min-width:1024px){.hero-section{padding-top:7rem;padding-bottom:7rem}}
.hero-title{font-size:2.25rem;font-weight:900;line-height:1.1;color:white;margin-bottom:1.5rem}
@media(min-width:640px){.hero-title{font-size:3rem}}
@media(min-width:1024px){.hero-title{font-size:3.75rem}}

/* Primary Button */
.btn-primary{display:inline-flex;align-items:center;justify-content:center;gap:.75rem;padding:1.5rem 2rem;border-radius:.5rem;font-weight:700;font-size:1.125rem;color:white;background:linear-gradient(to right,#2563eb,#1d4ed8);box-shadow:0 10px 15px -3px rgba(0,0,0,.1);text-decoration:none;transition:all .3s ease;border:0;cursor:pointer}
.btn-primary:hover{transform:translateY(-2px);box-shadow:0 20px 25px -5px rgba(0,0,0,.1)}

/* Utility Classes */
.sr-only{position:absolute;width:1px;height:1px;padding:0;margin:-1px;overflow:hidden;clip:rect(0,0,0,0);white-space:nowrap;border:0}
    `.trim();

    // Объединяем базовые стили с извлеченными
    const finalCSS =
      baseCriticalCSS +
      '\n\n/* Extracted Critical Styles */\n' +
      Array.from(allCriticalStyles).join('\n');

    // Записываем результат
    writeFileSync(config.outputPath, finalCSS, 'utf8');

    console.log(`✅ Критический CSS сгенерирован: ${config.outputPath}`);
    console.log(`📊 Общий размер: ${(finalCSS.length / 1024).toFixed(2)} KB`);
    console.log(`🎯 Стилей обработано: ${allCriticalStyles.size}`);
  } catch (error) {
    console.error('❌ Ошибка при генерации критического CSS:', error);
    process.exit(1);
  } finally {
    await browser.close();
  }
}

/**
 * Минифицирует CSS
 */
function minifyCSS(css) {
  return css
    .replace(/\/\*[\s\S]*?\*\//g, '') // Удаляем комментарии
    .replace(/\s+/g, ' ') // Заменяем множественные пробелы одним
    .replace(/;\s*}/g, '}') // Удаляем лишние точки с запятой
    .replace(/\s*{\s*/g, '{') // Убираем пробелы вокруг фигурных скобок
    .replace(/;\s*/g, ';') // Убираем пробелы после точки с запятой
    .replace(/,\s*/g, ',') // Убираем пробелы после запятых
    .trim();
}

// Запуск скрипта
if (process.argv[1] === __filename) {
  generateCriticalCSS().catch(console.error);
}

export { generateCriticalCSS, minifyCSS };
